package resources;

public class actions {

    public static void printArray(int[] arr){
        for(int k: arr){
            System.out.print(k + " ");
        }
        System.out.println(" ");
    }

    public static void printArray(String[] arr) {
        for(String k: arr){
            System.out.print(k + " ");
        }
        System.out.println(" ");
    }
}
